public class OccupantInCol {

    private Object occupant;
    private int col;

    public OccupantInCol(Object o, int c){
        occupant = o;
        col = c;
    }

    public Object getOccupant(){
        return occupant;
    }

    public void setOccupant(Object o){
        occupant = o;
    }

    public int getCol(){
        return col;
    } 

}   
